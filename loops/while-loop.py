# While Loop
# While loop repeatedly executes a target statement as long as a given condition becomes true.

count = 1

while count < 4:
    print(count)
    count = count + 1

print("Out of while loop")
print("------------------------------")

# Using else Statement with While Loop

count = 0
while count < 4:
    print(count)
    count = count + 1
else:
    print("count is less than 4")
